package com.cozarb.service.textmessage.provider.bean;

public class RecipientMessage {
	String id;
	String recipient;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRecipient() {
		return recipient;
	}

	public void setRecipient(String recipient) {
		this.recipient = recipient;
	}

}
